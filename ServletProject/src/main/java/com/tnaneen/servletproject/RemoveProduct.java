        int j=0;
        for(int i=0;i<products.size();i++)
        {
            if(products.get(i).getProductId()==removed)
            {
                j=i;
                break;
            }
        }
        products.remove(j);
        session.setAttribute("MyShoppingCart", products);
        response.sendRedirect("cart.jsp?removed=true");
